//=============================================================================
// 初期化処理
//=============================================================================
void InitOption(void)
{
	InitOptionBG();
	InitPlayer();
	InitCollColor();

	//オーディオの初期化
	SOUND_RECORD* pSoundRecord = GetSoundRecord();
	PlaySound(pSoundRecord->BGMGameID, SOUND_LOOP);
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitOption(void)
{
	UninitOptionBG();
	UninitPlayer();
	StopSoundAll();
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateOption(void)
{
	UpdateOptionBG();
	UpdatePlayer();
	UpdateCollision();
	UpdateVolume();
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawOption(void)
{
	DrawOptionBG();
	DrawPlayer();
}